public class LiteralsDemo {

  public static void main(String[] args) {
    System.out.println("First Line\nSecond Line");
    System.out.println("Single Quote: '\n"
        + "Double Quote: \"\n"
        + "Backslash: \\\n"
        + "Carriage Return\n here\n"
        + "New Line: \n see?\n"
        + "Form Feed:\ffeed\n"
        + "Horizontal tab: \tA\tB\n"
        + "Backspace: \b spaced back\n"
        + "Octal 111 \111\n"
        + "Hexa 00FF: \u00FF");
    System.out.println("Tab Demo\nA\tB\tC\nD\tE\tF");

  }
}